package tinynest.dto;



/*
이름         널?       유형            
---------- -------- ------------- 
ID         NOT NULL VARCHAR2(20)  - 아이디  
PW                  VARCHAR2(100) - 비밀번호 
NAME                VARCHAR2(20)  - 이름
ZIPCODE             VARCHAR2(5)   - 우편번호
ADDRESS1            VARCHAR2(200) - 기본주소
ADDRESS2            VARCHAR2(200) - 상세주소
PHONE               VARCHAR2(30)  - 전화번호
EMAIL               VARCHAR2(50)  - 이메일
STATUS              NUMBER(1)     - 회원상태 : 0(탈퇴회원), 1(일반회원), 9(관리자)      
*/

public class MemberDTO {
	private String id;	
	private String pw;	
	private String name;			
	private String zipcode;	
	private String address1;	
	private String address2;
	private String phone;
	private String email;
	private int status;
	
	public MemberDTO() {
		// TODO Auto-generated constructor stub
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getPw() {
		return pw;
	}

	public void setPw(String pw) {
		this.pw = pw;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getZipcode() {
		return zipcode;
	}

	public void setZipcode(String zipcode) {
		this.zipcode = zipcode;
	}

	public String getAddress1() {
		return address1;
	}

	public void setAddress1(String address1) {
		this.address1 = address1;
	}

	public String getAddress2() {
		return address2;
	}

	public void setAddress2(String address2) {
		this.address2 = address2;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

}


